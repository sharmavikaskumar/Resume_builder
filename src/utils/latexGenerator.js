function esc(str = "") {
  return String(str)
    .replace(/\\/g,  "\\textbackslash{}")
    .replace(/&/g,   "\\&")
    .replace(/%/g,   "\\%")
    .replace(/\$/g,  "\\$")
    .replace(/#/g,   "\\#")
    .replace(/_/g,   "\\_")
    .replace(/\{/g,  "\\{")
    .replace(/\}/g,  "\\}")
    .replace(/~/g,   "\\textasciitilde{}")
    .replace(/\^/g,  "\\textasciicircum{}")
    .trim();
}

function toBullets(text = "") {
  const lines = text
    .split("\n")
    .map((l) => l.replace(/^[•\-]\s*/, "").trim())
    .filter(Boolean);
  if (!lines.length) return "";
  return [
    "      \\resumeItemListStart",
    ...lines.map((l) => `        \\resumeItem{${esc(l)}}`),
    "      \\resumeItemListEnd",
  ].join("\n");
}

function makeLink(url = "", display = "") {
  if (!url) return "";
  const href = url.startsWith("http") ? url : `https://${url}`;
  return `\\href{${href}}{\\underline{${esc(display || url)}}}`;
}

