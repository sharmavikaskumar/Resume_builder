function buildSummary(personal = {}) {
  if (!personal.summary) return "";
  return `%-----------SUMMARY-----------
\\section{Summary}
  \\resumeSubHeadingListStart
    \\resumeSubItem{${esc(personal.summary)}}
  \\resumeSubHeadingListEnd`;
}

function buildEducation(education = []) {
  const entries = education.filter((e) => e.institution || e.degree);
  if (!entries.length) return "";

  const rows = entries.map((e) => {
    const dateRange = [e.startYear, e.endYear].filter(Boolean).join(" -- ");
    const gpaLine   = e.gpa
      ? `\n      \\resumeItemListStart\n        \\resumeItem{GPA: ${esc(e.gpa)}}\n      \\resumeItemListEnd`
      : "";
    return `    \\resumeSubheading
      {${esc(e.institution)}}{}
      {${esc(e.degree)}}{${esc(dateRange)}}${gpaLine}`;
  });

  return `%-----------EDUCATION-----------
\\section{Education}
  \\resumeSubHeadingListStart
${rows.join("\n")}
  \\resumeSubHeadingListEnd`;
}

function buildExperience(experience = []) {
  const entries = experience.filter((e) => e.company || e.role);
  if (!entries.length) return "";

  const rows = entries.map((e) => {
    const dateRange = [e.startDate, e.endDate].filter(Boolean).join(" -- ");
    const bullets   = toBullets(e.description);
    return `    \\resumeSubheading
      {${esc(e.role)}}{${esc(dateRange)}}
      {${esc(e.company)}}{}
${bullets}`;
  });

  return `%-----------EXPERIENCE-----------
\\section{Experience}
  \\resumeSubHeadingListStart
${rows.join("\n\n")}
  \\resumeSubHeadingListEnd`;
}

function buildProjects(projects = []) {
  const entries = projects.filter((p) => p.name);
  if (!entries.length) return "";

  const rows = entries.map((p) => {
    const techPart = p.techStack ? `\\emph{${esc(p.techStack)}}` : "";
    const linkPart = p.link
      ? ` $|$ \\href{${p.link.startsWith("http") ? p.link : "https://" + p.link}}{\\faExternalLink*\\ \\small Link}`
      : "";
    const namePart = `\\textbf{${esc(p.name)}}`;
    const heading  = [namePart, techPart].filter(Boolean).join(" $|$ ");
    const bullets  = toBullets(p.description);
    return `      \\resumeProjectHeading
          {${heading}${linkPart}}{}
${bullets}`;
  });

  return `%-----------PROJECTS-----------
\\section{Projects}
    \\resumeSubHeadingListStart
${rows.join("\n")}
    \\resumeSubHeadingListEnd`;
}

function buildSkills(skills = []) {
  if (!skills.length) return "";

  const labels = ["Languages", "Frameworks", "Developer Tools", "Libraries"];
  const size   = Math.ceil(skills.length / 4);
  const chunks = [];
  for (let i = 0; i < skills.length; i += size) {
    chunks.push(skills.slice(i, i + size));
  }

  const rows = chunks
    .map((chunk, i) => {
      const label = labels[i] || `Skills ${i + 1}`;
      return `     \\textbf{${label}}{: ${chunk.map(esc).join(", ")}}`;
    })
    .join(" \\\\\n");

  return `%
%-----------PROGRAMMING SKILLS-----------
\\section{Technical Skills}
 \\begin{itemize}[leftmargin=0.15in, label={}]
    \\small{\\item{
${rows}
    }}
 \\end{itemize}`;
}

function buildCertifications(certifications = []) {
  const entries = certifications.filter((c) => c.name);
  if (!entries.length) return "";

  const rows = entries.map((c) => {
    const dateStr = [c.issueDate, c.expiryDate].filter(Boolean).join(" -- ");
    return `    \\resumeSubheading
      {${esc(c.name)}}{${esc(dateStr)}}
      {${esc(c.issuer)}}{}`;
  });

  return `%-----------CERTIFICATIONS-----------
\\section{Certifications}
  \\resumeSubHeadingListStart
${rows.join("\n")}
  \\resumeSubHeadingListEnd`;
}

function buildAchievements(achievements = []) {
  const entries = achievements.filter((a) => a.title);
  if (!entries.length) return "";

  const items = entries.map((a) => {
    const year = a.year        ? ` (${esc(a.year)})`         : "";
    const desc = a.description ? ` -- ${esc(a.description)}` : "";
    return `    \\resumeItem{\\textbf{${esc(a.title)}}${year}${desc}}`;
  });

  return `%-----------ACHIEVEMENTS-----------
\\section{Achievements}
  \\resumeItemListStart
${items.join("\n")}
  \\resumeItemListEnd`;
}

