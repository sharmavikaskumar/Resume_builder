export function generateLatex(data = {}) {
  const {
    personal       = {},
    contact        = {},
    education      = [],
    experience     = [],
    skills         = [],
    projects       = [],
    certifications = [],
    achievements   = [],
  } = data;

  const sections = [
    buildHeader(personal, contact),
    buildSummary(personal),
    buildEducation(education),
    buildExperience(experience),
    buildProjects(projects),
    buildSkills(skills),
    buildCertifications(certifications),
    buildAchievements(achievements),
  ].filter(Boolean).join("\n\n\n");

  return `${PREAMBLE}\n\n%-------------------------------------------\n%%%%%%  RESUME STARTS HERE  %%%%%%%%%%%%%%%%%%%%%%%%%%%%\n\n\n\\begin{document}\n\n${sections}\n\n%-------------------------------------------\n\\end{document}`;
}

export async function copyToClipboard(text) {
  try {
    await navigator.clipboard.writeText(text);
    return true;
  } catch {
    return false;
  }
}

export function openInOverleaf(latexCode) {
  const form  = document.createElement("form");
  form.method = "POST";
  form.action = "https://www.overleaf.com/docs";
  form.target = "_blank";
  const input = document.createElement("input");
  input.type  = "hidden";
  input.name  = "snip";
  input.value = latexCode;
  form.appendChild(input);
  document.body.appendChild(form);
  form.submit();
  document.body.removeChild(form);
}